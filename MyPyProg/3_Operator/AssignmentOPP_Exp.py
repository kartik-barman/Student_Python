#example which shows all the above assignment operations:
# Assignment Operator
a = 10

# Addition Assignment
a += 5
print ("a += 5 : ", a)      # O/P: 15

# Subtraction Assignment
a -= 5
print ("a -= 5 : ", a)      # O/P: 10

# Multiplication Assignment
a *= 5
print ("a *= 5 : ", a)      # O/P: 50

# Division Assignment
a=13
a /= 4   # VARIABLE a BECOMES FLOAT
print ("a /= 5 : ",a)       # O/P: 3.25

# Remainder Assignment
a %= 3
print ("a %= 3 : ", a)      # O/P: 0.25

a=14
a %= 3
print ("a %= 3 : ", a)      # O/P: 2

# Exponent Assignment
a=5
a **= 2
print ("a **= 2 : ", a)     # O/P: 25

# Floor Division Assignment
a //= 3
print ("a //= 3 : ", a)     # O/P: 8
